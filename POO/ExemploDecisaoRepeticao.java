public class ExemploDecisaoRepeticao {
    public static void main (String[] args){
        int d1 = 2;
        int d2 = 1;

        if (d1 <= d2) {
            System.out.println("o Dado 1 e menor ou igual ao Dado 2");
        }

        else{
            System.out.println("o Dado 1 e maior que o Dado 2");
        } 

    System.out.println();
    System.out.println("Exemplo de For");
    for (int i = 0; i < 10; i++){
        System.out.println("i: " + i);
    }
    System.out.println();
    System.out.println("Exemplo de While");
    int j = 5;
    while (j < 10){
        System.out.println("j: " + j);
        j++;
    }
    }
} 

